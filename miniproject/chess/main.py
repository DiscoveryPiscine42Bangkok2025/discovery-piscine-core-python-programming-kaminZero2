from checkmate import checkmate
# กำหนดโครงสร้างกระดานในรูปแบบ String (หลายบรรทัด)
def main():
    board = """\
R..K
....
..P.
...."""
# เรียกฟังก์ชัน checkmate เพื่อตรวจสอบกระดาน
    checkmate(board)
# ในตัวอย่างนี้ กระดานมีคิง (K) อยู่ที่ตำแหน่ง (0, 3) และมีเรือ (R) อยู่ที่ตำแหน่ง (0, 0) ซึ่งสามารถโจมตีคิงได้ ดังนั้นผลลัพธ์จะเป็น "Check mate"
if __name__ == "__main__":
    main()